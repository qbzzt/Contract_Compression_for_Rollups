async function main() {
  // Hardhat always runs the compile task when running scripts with its command
  // line interface.
  //
  // If this script is run directly using `node` you may want to call compile
  // manually to make sure everything is compiled
  // await hre.run('compile');

  // We get the contract to deploy
  const Deployer = await hre.ethers.getContractFactory("DecompressingDeployer");
  const deployer = await Deployer.deploy()

  await deployer.deployed()
  console.log(`Deployer deployed to: ${deployer.address}`)

  const gas = await deployer.estimateGas.deployCompressed(compressedGreeter)

  console.log(`Gas estimated: ${gas}`)

  const tx1 = await deployer.deployCompressed(
    compressedGreeter,
    {gasLimit: 1000000000}
  )
  console.log(`Deploy transaction sent`)
  const receipt1 = await tx1.wait()
  console.log(`Deploy transaction received`)

  console.log(`Results: ${receipt1.events[0].args}`)
}

// We recommend this pattern to be able to use async/await everywhere
// and properly handle errors.
main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });
